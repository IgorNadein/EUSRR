/**
 * Функция для поиска эмодзи по русскому запросу
 */
export function searchEmojiRussian(query) {
  if (!query) return [];
  
  const lowerQuery = query.toLowerCase().trim();
  const results = [];
  
  for (const [emoji, keywords] of Object.entries(emojiRussianKeywords)) {
    if (keywords.some(keyword => keyword.includes(lowerQuery))) {
      results.push(emoji);
    }
  }
  
  return results;
}
